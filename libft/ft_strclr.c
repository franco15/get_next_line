#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int l;

	i = 0;
	l = 0;
	if (s)
	{
		while (s[l] != '\0')
			l++;
		while (l >= 0)
		{
			s[i] = '\0';
			i++;
			l--;
		}
	}
}
