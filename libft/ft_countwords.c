#include "libft.h"

int		ft_countwords(char const *s, char c)
{
	int i;

	i = 0;
	if (!s)
		return (0);
	while (*s)
	{
		if (*s != c)
			i++;
		while (*s != c && *s)
			s++;
		if (!*s)
			return (i);
		s++;
	}
	return (i);
}
