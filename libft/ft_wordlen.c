#include "libft.h"

int		ft_wordlen(char const *s, char c, int k)
{
	int	i;

	i = 0;
	while (s[k])
	{
		if (s[k] == c)
			k++;
		else
		{
			k++;
			i++;
		}
	}
	return (i);
}
